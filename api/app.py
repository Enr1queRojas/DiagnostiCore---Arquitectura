@app.post(
    "/api/runs",
    response_model=RunCreateResponse,
    status_code=201,
    summary="Create a new diagnostic run",
)
async def create_run(body: RunCreateRequest) -> RunCreateResponse:
    """
    Create a new DiagnostiCore diagnostic run.

    Returns a run_id and a scoped JWT Bearer token.
    Pass the token in the Authorization header for all subsequent calls to this run.
    """
    api_key = os.environ.get("ANTHROPIC_API_KEY", "").strip()
    if not api_key:
        raise HTTPException(
            status_code=503,
            detail="ANTHROPIC_API_KEY environment variable is not set on the server.",
        )

    bb = Blackboard.crear_run(
        nombre_cliente=body.cliente,
        sector=body.sector,
        consultor=body.consultor,
        tamaño=body.tamanio,
        empleados=body.empleados,
        runs_dir=_get_runs_dir(),
    )
    run_id: str = bb._data["run_id"]

    token = create_run_token(
        run_id=run_id,
        consultor_id=body.consultor,
        ttl_hours=body.token_ttl_hours,
    )
    claims = verify_run_token(token)  # decode to get expires_at

    return RunCreateResponse(
        run_id=run_id,
        token=token,
        created_at=datetime.now(tz=timezone.utc).isoformat(),
        token_expires_at=claims.expires_at.isoformat(),
    )


@app.get(
    "/api/runs/{run_id}",
    response_model=RunStatusResponse,
    summary="Get run status",
)
async def get_run_status(run_id: str = Depends(_verify_run_access)) -> RunStatusResponse:
    """Current state of a diagnostic run including per-agent completion status."""
    bb = _load_blackboard(run_id)
    sintesis = bb.get_sintesis()
    idd = sintesis.get("idd") if sintesis else None
    errores: list[dict[str, Any]] = bb._data.get("metadatos", {}).get("errores", [])

    return RunStatusResponse(
        run_id=run_id,
        estado=bb.get_estado(),
        cliente=bb.get_cliente(),
        agentes_completados=bb.agentes_completados(),
        agents=_build_agents_status(bb),
        errores=errores,
        idd=idd,
    )


@app.post(
    "/api/runs/{run_id}/pipeline",
    response_model=PipelineStartResponse,
    status_code=202,
    summary="Start the diagnostic pipeline",
)
async def start_pipeline(
    body: PipelineStartRequest,
    run_id: str = Depends(_verify_run_access),
) -> PipelineStartResponse:
    """
    Trigger the full A1→A8 pipeline as a background task.

    Returns 202 immediately. Connect to GET /api/runs/{run_id}/stream to
    receive real-time progress via SSE.

    Returns 409 if a pipeline is already running for this run.
    """
    api_key = os.environ.get("ANTHROPIC_API_KEY", "").strip()
    if not api_key:
        raise HTTPException(
            status_code=503,
            detail="ANTHROPIC_API_KEY is not set on the server.",
        )

    # Verify run exists
    _load_blackboard(run_id)

    # Prevent double-start
    existing_task = _pipeline_tasks.get(run_id)
    if existing_task and not existing_task.done():
        raise HTTPException(
            status_code=409,
            detail=f"Pipeline for run '{run_id}' is already running.",
        )

    task = asyncio.create_task(
        _run_pipeline_task(
            run_id=run_id,
            model=body.model,
            api_key=api_key,
            runs_dir=_get_runs_dir(),
        ),
        name=f"pipeline-{run_id}",
    )
    _pipeline_tasks[run_id] = task

    logger.info("Pipeline task created | run=%s | model=%s", run_id, body.model)
    return PipelineStartResponse(
        run_id=run_id,
        status="started",
        message=(
            f"Pipeline started for run '{run_id}'. "
            f"Connect to GET /api/runs/{run_id}/stream for real-time progress."
        ),
    )


@app.get(
    "/api/runs/{run_id}/stream",
    summary="SSE real-time pipeline progress",
    response_description="text/event-stream",
)
async def stream_pipeline_events(
    request: Request,
    run_id: str = Depends(_verify_run_access),
) -> EventSourceResponse:
    """
    Subscribe to Server-Sent Events for a running pipeline.

    Each event has the form:
      event: <type>
      data: <JSON object>

    The stream closes automatically when the pipeline completes or errors.
    """
    async def _event_generator():
        async for evt in event_bus.subscribe(run_id):
            # SSE keep-alive: check client disconnect on every event
            if await request.is_disconnected():
                logger.debug("SSE client disconnected | run=%s", run_id)
                return
            yield {
                "event": evt["type"],
                "data": json.dumps(evt["data"], ensure_ascii=False),
                "id": evt["timestamp"],
            }

    return EventSourceResponse(_event_generator())


@app.get(
    "/api/runs/{run_id}/report",
    response_model=ReportResponse,
    summary="Get final diagnostic report",
)
async def get_report(run_id: str = Depends(_verify_run_access)) -> ReportResponse:
    """
    Retrieve the final diagnostic report once the pipeline has completed.

    Returns 202 if the pipeline is still running, 404 if the run doesn't exist.
    """
    bb = _load_blackboard(run_id)

    if bb.get_estado() not in ("completado",):
        raise HTTPException(
            status_code=202,
            detail=(
                f"Run '{run_id}' is not yet complete (estado: {bb.get_estado()}). "
                f"Poll GET /api/runs/{run_id} or subscribe to the SSE stream."
            ),
        )

    sintesis = bb.get_sintesis() or {}
    idd: float | None = sintesis.get("idd")
    idd_clasificacion: str | None = sintesis.get("clasificacion")
    causas_raiz = sintesis.get("causas_raiz", [])
    one_pager = bb.get_one_pager()

    return ReportResponse(
        run_id=run_id,
        markdown_summary=bb.to_markdown_resumen(),
        idd=idd,
        idd_clasificacion=idd_clasificacion,
        causas_raiz_count=len(causas_raiz),
        one_pager_available=one_pager is not None,
    )


