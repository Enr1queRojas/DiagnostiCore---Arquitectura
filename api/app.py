# ─────────────────────────────────────────────────────────────────────────────
# Pipeline task registry
# Tracks running asyncio.Task per run_id to prevent duplicate pipeline starts.
# ─────────────────────────────────────────────────────────────────────────────

_pipeline_tasks: dict[str, asyncio.Task] = {}


# ─────────────────────────────────────────────────────────────────────────────
# Application lifecycle
# ─────────────────────────────────────────────────────────────────────────────

@asynccontextmanager
async def _lifespan(application: FastAPI):
    """Startup: initialise telemetry. Shutdown: cancel running pipelines."""
    setup_telemetry()
    logger.info("DiagnostiCore API started")
    yield
    # Cancel any still-running pipeline tasks on shutdown
    for run_id, task in list(_pipeline_tasks.items()):
        if not task.done():
            logger.warning("Cancelling pipeline task for run %s on shutdown", run_id)
            task.cancel()
    logger.info("DiagnostiCore API stopped")


