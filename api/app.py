"""
api/app.py
===========
DiagnostiCore FastAPI application with REST endpoints and SSE streaming.

Endpoints:
  POST /api/runs
    Create a new diagnostic run. Returns run_id + JWT Bearer token.

  GET  /api/runs/{run_id}
    Current run status: estado, agents, IDD (if available).
    Requires Bearer token when DIAGNOSTICORE_JWT_SECRET is set.

  POST /api/runs/{run_id}/pipeline
    Trigger the full diagnostic pipeline as a background asyncio task.
    Returns 202 immediately; progress is streamed via GET /stream.
    Requires Bearer token.

  GET  /api/runs/{run_id}/stream
    Server-Sent Events stream. Each event has:
      event: <type>     (agent_start | agent_done | pipeline_done | …)
      data:  <JSON>
    Closes automatically when the pipeline finishes.
    Requires Bearer token.

  GET  /api/runs/{run_id}/report
    Returns the final markdown summary and IDD once the pipeline completes.
    Requires Bearer token.

Authentication:
  Pass: Authorization: Bearer <token>
  Token is generated by POST /api/runs and printed by main.py on new run creation.
  When DIAGNOSTICORE_JWT_SECRET is NOT set, auth is advisory only (warns but allows).

Run:
  uvicorn api.app:app --host 0.0.0.0 --port 8000
"""

