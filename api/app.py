# Auth dependency
# ─────────────────────────────────────────────────────────────────────────────

def _extract_bearer(authorization: str | None) -> str | None:
    """Parse 'Bearer <token>' header value."""
    if authorization and authorization.startswith("Bearer "):
        return authorization[7:].strip()
    return None


async def _verify_run_access(
    run_id: str,
    authorization: Annotated[str | None, Header()] = None,
) -> str:
    """
    FastAPI dependency that enforces run-scoped token access.

    - If DIAGNOSTICORE_JWT_SECRET is set: token required and verified.
    - If secret is not set: token optional; logs a warning if missing.
    """
    token = _extract_bearer(authorization)
    secret_configured = bool(os.environ.get("DIAGNOSTICORE_JWT_SECRET", "").strip())

    if not token:
        if secret_configured:
            raise HTTPException(
                status_code=401,
                detail="Authorization: Bearer <token> header is required.",
            )
        logger.warning(
            "Unauthenticated access to run %s — set DIAGNOSTICORE_JWT_SECRET to enforce tokens",
            run_id,
        )
        return run_id

    try:
        verify_run_token(token, expected_run_id=run_id)
    except TokenError as exc:
        raise HTTPException(status_code=401, detail=str(exc))

    return run_id


# ─────────────────────────────────────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────────────────────────────────────

def _get_runs_dir() -> str:
    return os.environ.get("DIAGNOSTICORE_RUNS_DIR", "runs")


def _load_blackboard(run_id: str) -> Blackboard:
    """Load blackboard or raise 404."""
    try:
        return Blackboard(f"{_get_runs_dir()}/{run_id}.json")
    except BlackboardError:
        raise HTTPException(status_code=404, detail=f"Run '{run_id}' not found.")


def _build_agents_status(bb: Blackboard) -> list[AgentStatus]:
    """Build per-agent status list from blackboard data."""
    AGENT_MAP = [
        ("A1", "A1_estrategia"),
        ("A2", "A2_liderazgo"),
        ("A3", "A3_cultura"),
        ("A4", "A4_procesos"),
        ("A5", "A5_datos"),
        ("A6", "A6_tecnologia"),
        ("A7", None),
        ("A8", None),
    ]
    completed = bb.agentes_completados()
    statuses: list[AgentStatus] = []
    for agent_id, dim_key in AGENT_MAP:
        nivel = None
        ts = None
        if dim_key:
            dim_data = bb.get_resultado_dimensional(dim_key) if agent_id in completed else None
            if dim_data:
                nivel = dim_data.get("nivel_madurez")
        if agent_id in completed:
            ts_key = f"{agent_id}_completado"
            ts = bb._data.get("timestamps", {}).get(ts_key)
        statuses.append(AgentStatus(
            agent_id=agent_id,
            completed=agent_id in completed,
            nivel_madurez=nivel,
            timestamp=ts,
        ))
    return statuses


# ─────────────────────────────────────────────────────────────────────────────
# Pipeline background task
# ─────────────────────────────────────────────────────────────────────────────

async def _run_pipeline_task(
    run_id: str,
    model: str,
    api_key: str,
    runs_dir: str,
) -> None:
    """
    Coroutine executed as a background asyncio.Task.
    Runs the full pipeline and emits SSE events on completion/failure.
    The event_bus handles agent-level events internally via agent_runner.
    """
    logger.info("Background pipeline starting | run=%s | model=%s", run_id, model)
    try:
        llm_client = AsyncLLMClient(api_key=api_key, model=model)
        await run_full_pipeline(run_id=run_id, llm_client=llm_client, runs_dir=runs_dir)
        # pipeline_done event is emitted from run_full_pipeline via agent_runner
        logger.info("Background pipeline complete | run=%s", run_id)
    except OrchestratorError as exc:
        logger.error("Background pipeline error | run=%s | %s", run_id, exc)
        # Ensure SSE stream is closed even if the event was not emitted by runner
        event_bus.emit(run_id, "pipeline_error", {
            "run_id": run_id,
            "failed_agents": exc.failed_agents,
            "error": str(exc),
        })
    except Exception as exc:
        logger.exception("Unexpected pipeline error | run=%s", run_id)
        event_bus.emit(run_id, "pipeline_error", {
            "run_id": run_id,
            "failed_agents": [],
            "error": str(exc),
        })
    finally:
        _pipeline_tasks.pop(run_id, None)


